  public int tileWidth;
  public int tileHeight;
  private byte[] tiles;
  private String imagePath;
  private BufferedImage image;

  public BackgroundLayer(String imagePath) {
    if (imagePath != null) {
      this.imagePath = imagePath;
      this.loadLevelFromFile();
    } else {
      this.tileWidth = 256 / 8;
      this.tileHeight = 224 / 8;
      tiles = new byte[tileWidth * tileHeight];
      this.generateLevel();
    }
  }

  private void loadLevelFromFile() {
    try {
      this.image = ImageIO.read(BackgroundLayer.class.getResource(this.imagePath));
      this.tileWidth = this.image.getWidth();
      this.tileHeight = this.image.getHeight();
      tiles = new byte[tileWidth * tileHeight];
      this.loadTiles();
    } catch (IOException e) {
      e.printStackTrace();
    }
  }

  private void loadTiles() {
    int[] tileColours = this.image.getRGB(0, 0, tileWidth, tileHeight, null, 0, tileWidth);
    for (int y = 0; y < tileHeight; y++) {
      for (int x = 0; x < tileWidth; x++) {
        tileCheck:
        for (Tile t : TileContainer.tiles) {
          if (t != null && t.getLevelColour() == tileColours[x + y * tileWidth]) {
            this.tiles[x + y * tileWidth] = t.getId();
            break tileCheck;
          }
        }
      }
    }
  }

  @SuppressWarnings("unused")
  private void saveLevelToFile() {
    try {
      ImageIO.write(image, "png", new File(BackgroundLayer.class.getResource(this.imagePath).getFile()));
    } catch (IOException e) {
      e.printStackTrace();
    }
  }

  public void generateLevel() {
    for (int y = 0; y < tileHeight; y++) {
      for (int x = 0; x < tileWidth; x++) {
        int index = x + y * tileWidth;
        switch (mapData[index]) {
          case 0:
            tiles[index] = TileContainer.VOID.getId();
            break;
          case 1:
            tiles[index] = TileContainer.BB_STONE.getId();
            break;
        }
      }
    }
  }


  public void tick(int tickTime) {
    for (Tile t : TileContainer.tiles) {
      if (t == null) {
        break;
      }
      t.tick();
    }
  }

  /** Get a tile in tile space. */
  public Tile getTile(int x, int y) {
    if (0 > x || x >= tileWidth || 0 > y || y >= tileHeight)
      return TileContainer.VOID;
    return TileContainer.tiles[tiles[x + y * tileWidth]];
  }

  /** Get a tile in pixel space. */
  public Tile getTilePixelSpace(int pixelSpaceX, int pixelSpaceY) {
    return getTile(pixelSpaceX >> 8, pixelSpaceY >> 3);
  }

